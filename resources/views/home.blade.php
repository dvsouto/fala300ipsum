    const generate = function(pharagraphs = 3, words = 10){
      var result = first + ". ";

      for(var p = 1; p <= pharagraphs; p++)
      {
        for(var w = 1; w <= words; w++)
        {
          let item_key = Math.floor(Math.random() * dictionary.length)
          let item = dictionary[item_key];
          let more = "";

          if (item.substr(-1) != "?")
            more = ".";

          result += item + more + " ";
        }

        if (p < pharagraphs)
          result += "\n\n";
      }

      return result;
    };

